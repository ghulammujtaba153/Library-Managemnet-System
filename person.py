
class Person:
    def __int__(self, id, name, email, password, gender):
        self.id = id
        self.name = name
        self.email = email
        self.password = password
        self.gender = gender


    def setId(self, id):
        self.id=id

    def setId(self, name):
        self.name=name

    def setId(self, email):
        self.email=email

    def setId(self, password):
        self.password=password

    def setId(self, gender):
        self.gender=gender

